def get_line_name(hexagram_lines, position):
    """根据六爻结构和位置返回爻名。

    position: 1-6 (从下往上)
    阳爻称「九」,阴爻称「六」
    """
    is_yang = hexagram_lines[position - 1] == 1
    yao = "九" if is_yang else "六"
    if position == 1:
        return f"初{yao}"
    elif position == 6:
        return f"上{yao}"
    else:
        labels = ["", "二", "三", "四", "五", ""]
        return f"{yao}{labels[position - 1]}"


def cast():
    """随机起卦，返回卦信息和爻信息。"""
    # 随机选卦 (1/64) 和爻 (1/6)
    number, name, lower, upper = random.choice(HEXAGRAMS)
    lower_lines = TRIGRAMS[lower]
    upper_lines = TRIGRAMS[upper]
    # 六爻从下往上：下卦三爻 + 上卦三爻
    full_lines = lower_lines + upper_lines

    line_number = random.randint(1, 6)
    line_name = get_line_name(full_lines, line_number)
    is_changing = True  # 动爻

    return {
        "hexagram_number": number,
        "hexagram_name": name,
        "lower_trigram": lower,
        "upper_trigram": upper,
        "line_number": line_number,
        "line_name": line_name,
        "changing": is_changing,
    }


